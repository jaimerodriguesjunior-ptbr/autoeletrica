-- Função RPC para obter e incrementar o próximo número de forma atômica
CREATE OR REPLACE FUNCTION public.get_next_nfce_number(p_org_id UUID, p_serie INTEGER)
RETURNS INTEGER AS $$
DECLARE
    v_next_number INTEGER;
BEGIN
    -- Incrementa e retorna o novo valor em uma única operação atômica
    UPDATE public.nfce_sequences
    SET last_number = last_number + 1
    WHERE organization_id = p_org_id AND serie = p_serie
    RETURNING last_number INTO v_next_number;

    -- Se não encontrar o registro, cria um novo (Série 1, começa no 1)
    IF v_next_number IS NULL THEN
        INSERT INTO public.nfce_sequences (organization_id, serie, last_number)
        VALUES (p_org_id, p_serie, 1)
        ON CONFLICT (organization_id, serie) 
        DO UPDATE SET last_number = public.nfce_sequences.last_number + 1
        RETURNING last_number INTO v_next_number;
    END IF;

    RETURN v_next_number;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

