-- Tabela para controle de numeração sequencial de NFCe por loja e série
CREATE TABLE IF NOT EXISTS public.nfce_sequences (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    organization_id UUID NOT NULL REFERENCES public.organizations(id) ON DELETE CASCADE,
    serie INTEGER NOT NULL DEFAULT 1,
    last_number INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW(),
    UNIQUE(organization_id, serie)
);

-- Habilitar RLS
ALTER TABLE public.nfce_sequences ENABLE ROW LEVEL SECURITY;

-- Política para leitura
CREATE POLICY "Permitir leitura para membros da mesma org" ON public.nfce_sequences
    FOR SELECT
    USING (organization_id IN (
        SELECT organization_id FROM public.profiles WHERE id = auth.uid()
    ));

-- Política para inserção/update
CREATE POLICY "Permitir alteração para membros da mesma org" ON public.nfce_sequences
    FOR ALL
    USING (organization_id IN (
        SELECT organization_id FROM public.profiles WHERE id = auth.uid()
    ));

-- Função para atualizar o updated_at
CREATE OR REPLACE FUNCTION public.handle_nfce_sequences_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER on_nfce_sequences_updated
    BEFORE UPDATE ON public.nfce_sequences
    FOR EACH ROW EXECUTE FUNCTION public.handle_nfce_sequences_updated_at();

